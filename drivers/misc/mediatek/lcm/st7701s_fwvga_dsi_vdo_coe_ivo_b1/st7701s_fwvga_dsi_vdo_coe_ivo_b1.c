static void lcm_init(void)
{
    mt_set_gpio_out(0x46, 1);
    MDELAY(5);
    mt_set_gpio_out(0x46, 0);
    MDELAY(10);
    mt_set_gpio_out(0x46, 1);
    MDELAY(120);
    push_table(TABLE_lcm_init_lcm_resume, 37);
}

static void lcm_suspend(void)
{
    push_table(TABLE_lcm_suspend, 5);
    mt_set_gpio_out(0x46, 0);
}

static void lcm_resume(void)
{
    mt_set_gpio_out(0x46, 1);
    MDELAY(5);
    mt_set_gpio_out(0x46, 0);
    MDELAY(10);
    mt_set_gpio_out(0x46, 1);
    MDELAY(10);
    push_table(TABLE_lcm_init_lcm_resume, 37);
}

